public class DataTypes {
	public static void main(String[] args) {
		int num = 30;
		System.out.println("INT DATATYPE:" + num);
		char c = 'S';
		System.out.println("CHAR DATATYPE:" + c);
		float amount = 2067.7f;
		System.out.println("FLOAT DATATYPE:" + amount);
		double sal = 200000.0;
		System.out.println("DOUBLE DATATYPE:" + sal);
		long n = 100000L;
		System.out.println("LONG DATATYPE:" + n);
		boolean flag = true;
		System.out.println("BOOLEAN DATATYPE:" + flag);
		System.out.println("BOOLEAN DATATYPE:" + !flag);
		short s = 3000;
		System.out.println("SHORT DATATYPE:" + s);
		byte b = 30;
		System.out.println("BYTE DATATYPE:" + b);
		String a = "Kaur";
		System.out.println("STRING DATATYPE:" +a);
	}

}
