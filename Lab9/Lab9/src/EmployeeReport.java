public class EmployeeReport {
	public int empId;
	public String empName;
	public String empRole;
	public int empBasic;
	public int empHra;
	public int allowancePercentage;
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpRole() {
		return empRole;
	}
	public void setEmpRole(String empRole) {
		this.empRole = empRole;
	}
	public int getEmpBasic() {
		return empBasic;
	}
	public void setEmpBasic(int empBasic) {
		this.empBasic = empBasic;
	}
	public int getEmpHra() {
		return empHra;
	}
	public void setEmpHra(int empHra) {
		this.empHra = empHra;
	}
	public int getAllowancePercentage() {
		return allowancePercentage;
	}
	public void setAllowancePercentage(int allowancePercentage) {
		this.allowancePercentage = allowancePercentage;
	}
	

}
